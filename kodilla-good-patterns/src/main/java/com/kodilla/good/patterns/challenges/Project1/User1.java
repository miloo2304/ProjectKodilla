package com.kodilla.good.patterns.challenges.Project1;

public class User1 {
    private final String name;
    private final String email;

    public User1(String name, String email) {
        this.name = name;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }
}

